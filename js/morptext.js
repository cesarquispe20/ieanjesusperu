function textille(val , cargo) {
      
  $('.tlt').textillate({
    selector: '.texts',
    loop: false,      
    minDisplayTime: 2000,      
    initialDelay: 0,      
    autoStart: true,      
    inEffects: [],      
    outEffects: [ 'hinge' ],
    in: {
      effect: val,
      delayScale: 1.5,
      delay: 50,
      sync: false,
      shuffle: false,
      reverse: false,
      callback: function () {}
    },
    out: {
      effect: 'hinge',
      delayScale: 1.5,
      delay: 50,
      sync: false,
      shuffle: false,
      reverse: false,
      callback: function () {}
    },
    callback: function () {},
    type: 'char'
  });

setTimeout(
    function() 
    {
        $('#titulodos').html('<h4 class="card-title tltl text-dark" style="font-size: 1.2vw" > '+cargo + '</h4> ');
        $('.tltl').textillate({
            selector: '.texts',
            loop: false,
            minDisplayTime: 2000,
            initialDelay: 0,
            autoStart: true,
            inEffects: [],
            outEffects: [ 'hinge' ],
          
            in: {
              effect: val,
              delayScale: 1.5,
              delay: 50,
              sync: false,
              shuffle: false,
              reverse: false,
              callback: function () {}
            },
          
            out: {
              effect: 'hinge',
              delayScale: 1.5,
              delay: 50,
              sync: false,
              shuffle: false,
              reverse: false,
              callback: function () {}
            },
    
            callback: function () {},
            type: 'char'
          });
    }, 500);

}





